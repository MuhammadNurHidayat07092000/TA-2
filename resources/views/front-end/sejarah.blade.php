        <h1 style="margin-top: 15%" class="text-center">Sejarah</h1>
        <hr>
        <strong>Perkembangan organisasi dan kelembagaan pemerintah yang diberikan kewenangan dalam penanganan narkotika</strong> sudah dimulai dari tahun 1971 dengan dikeluarkannya Instruksi
        Presiden Republik Indonesia (Inpres) Nomor 6 Tahun 1971 kepada Kepala Badan Koordinasi Intellijen Nasional (BAKIN) untuk menanggulangi 6 permasalahan nasional yang menonjol, yaitu pemberantasan uang palsu, penanggulangan penyalahgunaan narkoba, penanggulangan penyelundupan, penanggulangan kenakalan remaja, penanggulangan subversi, dan pengawasan orang asing.
        <br><br> 
        
        <strong>Badan Koordinasi Pelaksanaan Instruksi Presiden (Bakolak Inpres)</strong> adalah sebuah badan koordinasi kecil yang beranggotakan 25 instansi pemerintah terkait, yang berada di bawah komando dan bertanggung jawab kepada Kepala BAKIN. Badan ini tidak mempunyai wewenang operasional dan tidak secara spesifik mendapat alokasi anggaran dari APBN melainkan disediakan berdasarkan kebijakan internal BAKIN. Dalam pelaksanaan
        kegiatannya, Bakolak Inpres bekerja sama dengan departemen terkait antara lain Departemen Luar Negeri, Departemen Kesehatan, Departemen Sosial, Departemen Pendidikan dan Kebudayaan, Departemen Penerangan, Departemen Kehakiman, dan sejumlah instansi lainnya.Berdasarkan pada perkembangan kiprah Bakolak Inpres tersebut, pemerintah bersama dengan anggota DPR RI membuat peraturan yang mengatur tentang narkotika.Peraturan tersebut adalah UU Nomor 9 Tahun 1976. Undang-undang tersebut lahir setelah
        sebelumnya DPR meratifikasi UN Single Convention on Narcotic Drugs 1961 dan diamandemen dengan protocol 1972 menjadi Undang-undang Nomor 8 Tahun 1976. 
        <br><br>
        
        <strong>Tahun 1997 Pemerintah Indonesia dan DPR RI mengesahkan dua peraturan</strong> yang berkaitan dengan penanggulangan bahaya Narkotika yaitu Undang-Undang Nomor 5 Tahun 1997 tentang Psikotropika dan Undang-Undang Nomor 22 Tahun 1997 tentang Narkotika.Berdasarkan kedua undang-undang tersebut, pada tahun 1999 pemerintah melalui Keputusan Presiden Nomor 116 Tahun 1999 membentuk Badan Koordinasi Narkotika Nasional atau disingkat BKNN. BKNN adalah suatu Badan Koordinasi penanggulangan Narkoba yang beranggotakan 25 Instansi Pemerintah terkait. BKNN secara ex-officio diketuai oleh Kepala Kepolisian Republik Indonesia (Kapolri). Namun demikian guna menjalankan pelaksanaan 
        tugas sehari-hari pemerintah membuat jabatan Kepala Pelaksana Harian (Kalakhar).Pelaksanaan BKNN berlangsung hingga tahun 2002 dan mengalami dua masa periode kepala pelaksana harian. Kalakhar BKNN pertama kali di jabat oleh Drs. Achwil Lutan, SH dan kemudian dilanjutkan oleh Drs. Daâ€™I Bachtiar. 
        <br><br>
        
        <strong>BKNN sebagai badan koordinasi</strong> dianggap tidak cukup memadai untuk menghadapi sindikat Narkoba yang makin serius menebar ancamannya. Hal tersebut dikarenakan BKNN tidak mempunyai anggaran dan personil sendiri yang dapat diandalkan guna menyelesaikan
        permasalahan Narkoba tersebut. Dibutuhkan lembaga dengan kewenangan dan kemampuan 
        yang lebih besar guna mengatasi permasalahan Narkoba. Karena itulah, melalui Keputusan 
        Presiden Nomor 17 Tahun 2002 tentang Badan Narkotika Nasional dan Instruksi Presiden
        Nomor 3 Tahun 2002 tentang penanggulangan penyalahgunaan dan peredaran gelap
        narkotika, psikotropika, prekursor, dan zat adiktif lainnya serta TAP MPR No. VI/MPR/2002
         tentang Rekomendasi atas laporan Pelaksanaan Putusan MPR RI tahun 2002 maka pada
        tanggal 22 Maret 2002 Badan Koordinasi Nasional (BKNN) diubah menjadi Badan Narkotika Nasional (BNN) dengan memiliki 25 anggota dari departemen dan lembaga pemerintah terkait.Kapolri selaku ketua Ex Officio bertanggungjawab langsung kepada Presiden. 
        <br><br>
        
        <strong>Kepala Pelaksana Harian BNN</strong> yang pertama dijabat oleh Drs. Nurfaizi.Menyikapi perkembangan permasalahan narkoba yang terus meningkat dan semakin serius, maka melalui Sidang Umum Majelis Permusyawaratan Rakyat Tahun 2002mengeluarkan Ketetapan MPR-RI Nomor : VI/MPR/2002 tentang Rekomendasi kepada DPR-RI dan Presiden RI untuk melakukan perubahan atas Undang-Undang Nomor 22 Tahun
        1997 tentang Narkotika dalam rangka efektifitas penanganan narkoba. Walaupun telah ada Undang-Undang Nomor 22 tahun 1997 tentang Narkotika namun dalam Undang-Undang tersebut belum mengatur kelembagaan yang menangani masalah narkoba secara efektif, oleh karena itu Pemerintah mengeluarkan Peraturan Presiden
        Nomor 83 Tahun 2007 tentang Badan Narkotika Nasional, Badan Narkotika Provinsi (BNP)
        dan Badan Narkotika Kabupaten/Kota (BNK/Kota), yang memiliki kewenangan operasional melalui Anggota BNN/BNP/BNK/Kota dengan instansi terkait. Adapun pertanggungjawaban masing-masing adalah sebagai berikut:
        <br><br>
        
        <strong>BNN bertanggung jawab kepada Presiden, BNP kepada Gubernur dan BNK/Kota kepada Bupati/Walikota</strong>, secara organisatoris institusi tersebut tidak mempunyai hubungan struktural-vertikal.Pada implementasinya Undang-Undang Nomor 22 Tahun 1997 tentang Narkotika
        dan Peraturan Presiden Nomor 83 Tahun 2007 tentang Badan Narkotika Nasional, Badan
        Narkotika Provinsi (BNP) dan Badan Narkotika Kabupaten/Kota (BNK/Kota) kurang efektif.
        Dalam Undang-Undang tersebut telah mengatur pembentukan kelembagaan dan kewenangan
        BNN dibidang penyelidikan serta penyidikan tindak pidana narkotika dan prekursor
        narkotika.Dalam UU tersebut status kelembagaan BNN ditetapkan menjadi Lembaga Pemerintah Non-Kementerian (LPNK) dengan struktur vertikal ke Provinsi dan Kabupaten/Kota. Di Provinsi dibentuk BNN Provinsi, dan di Kabupaten/Kota dibentuk BNN Kabupaten/Kota. BNN dipimpin oleh seorang Kepala BNN yang diangkat dan diberhentikan oleh Presiden. BNN berkedudukan di bawah dan bertanggungjawab kepada Presiden. Kepala BNN dibantu oleh seorang Sekretaris Utama, Inspektur Utama, dan 5 (lima) Deputi yaitu Deputi Bidang Pencegahan, Deputi Bidang Pemberdayaan Masyarakat, Deputi Bidang
        Rehabilitasi, Deputi Bidang Pemberantasan, dan Deputi Bidang Hukum dan Kerjasama.Dalam rangka pelaksanaan Undang-Undang Nomor 35 Tahun 2009 tentang Narkotika, Pemerintah mengeluarkan Peraturan Presiden Nomor 23 Tahun 2010 tentang Badan Narkotika Nasional, Peraturan Kepala BNN Nomor : KEP/03/V/2010/BNN tentang Organisasi dan Tata Kerja Badan Narkotika Nasional, Peraturan Kepala BNN Nomor : KEP/04/V/ 2010/BNN tentang Organisasi dan Tata Kerja Badan Narkotika Nasional Provinsi dan Badan Narkotika Nasional Kabupaten/Kota. Badan Narkotika Nasional telah memiliki organisasi vertical hingga ke tingkat kabupaten/kota. Lembaga vertical BNN hingga ke
        wilayah propinsi disebut sebagai Badan Narkotika Nasional Propinsi (BNNP) yang saat ini telah berdiri di seluruh propinsi di Indonesia. Lembaga vertical BNN di tingkat kabupaten/kota dinamakan Badan Narkotika Nasional Kabupaten/Kota (BNNK). Hingga saat ini telah berdiri 173 BNNK.<span>Â </span>
